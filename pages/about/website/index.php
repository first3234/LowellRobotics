<h1>About Our Website</h1>
<h3 style="width:795px;">History</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Our team has held the domain lowellrobotics.org since before we became a FIRST team. When it was purchased, we had a small VEX hobby group
so the website wasn't technically advanced, but it was usefull. When we joined FIRST the following year, our focus wasn't on the website. It wasn't until last year's
competition that we decided to overhaul the website. At the time, a few designs were tried, with limited success, then Zachary DeBruine arrived with the design you see
now.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;It was wonderful, but it still needed some technical improvements. Caleb Pratt used PHP to template the header, sidebar, and footer of the page. He also helped Zach
condense the styles from a plethora of repeated in-line styles to a master CSS sheet. The file system was also revamped creating a standard way to add new pages to the
website. Zach added more content with input from many team members, and it was launched.</p>

<h3 style="width:795px;">Technical Specs and Details</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;We are firm believers in originality. Everything on this website (except the forum) was written from scratch in Notepad++.  All HTML,
CSS, JavaScript, and PHP is entirely hand-coded. We designed our webpage using the XHTML 1.0-Strict doctype. The website is W3 Valid for XHTML and CSS.</p>

