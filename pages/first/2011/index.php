<h1>Logomotion</h1>

<img src="/pages/first/2012/img/ReboundRumble.jpg" class="newspic" alt="Rebound Rumble" />

<p>&nbsp;&nbsp;&nbsp;&nbsp;This years game is all about- thats right, you guessed it- basketball! It isn't a normal game of basketball, however; besides the
balls and the hoops this game is much different than any game of basketball we've ever seen.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;The challenge will be played on a 27 by 54 foot carpeted area with an Alliance Station at either end. At each end of the
court there are four baskets: one up high, two in the middle, and one low. The goal for this year is to shoot foam basketballs into
these hoops, but, like all FIRST competitions, there is more to it than that. Across the middle of the court is a 4" tall divider,
seperating the court into two halves. In order to get to the other side of the court, players must make use of one of three "bridges",
tipping platforms that can be pushed down and driven across to get to the other side. Players may also attempt to cross the barricade
itself, however there is always a chance of getting hung up or tipped off of it. Penalties are also different this year, they now are
immediately added to the <b>other</b> team's score.</p>

<img src="/pages/first/2012/img/reboundField.jpg" class="newspic" alt="Field" style="float:left;" />

<p>&nbsp;&nbsp;&nbsp;&nbsp;Teams this year also have a little "endgame" competition to pick up some extra points as well. Toward the 
end of each match, teams are encouraged to balance two or more robots on the bridges to pick up these extra points. Points are awarded
for each successfully balanced bridge, and there is a considerable point boost given out if opposing alliance robots sucessfully balance
together on the center bridge. Teamwork between alliances may very well mean the difference between winning and losing for close matches.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;We all look forward to this years game, and one thing is for sure; it will be an interesting and event filled one!</p>

