<div class="titlebanner">
<h2>Team Update:  Week 2</h2>
</div>
<span style="font-weight:bold;">Jan. 23 2010</span>
<p>We have a design for the kicker. A cam will rotate to pull back the kicker, and then turn the little more needed to release the "foot" and kick the ball. The parts 
should come in soon. The preliminary arm assembly is in now too thanks to White's Bridge. It is made for the side of the robot, but that will work even better for 
hanging on the sides of the towers, which means hanging from the bump. Since the bump is higher, we won't have as hard of a time hanging.</p>

<div class="titlebanner">
<h2>Team Update:  Week 1</h2>
</div>
<span style="font-weight:bold;">Jan. 16 2010</span>
<p>One week in and we decided on a drive system, omni-wheels on the front and traction wheels on the back. This should help with maneuverability and get us up the bumps 
without tipping over. The base is almost together. Pool noodles were ordered for the bumpers.</p>

