<h1>Red Arrow Robotics Team 3234</h1>

<img src="/common/robot2.jpg" width=400 height=300 alt="Team" class="indexpic" />Welcome to the website of Team #3234, Red Arrow Robotics!  
Our team participates in the annual <a href="http://www.usfirst.org/roboticsprograms/frc" >FIRST Robotics Competition (FRC)</a> - a popular 
challenge among High Schools students which is designed to inspire and educate students and help prepare them to pursue and strive in higher education.  
The <a href="http://www.usfirst.org">FIRST</a> (For Inspiration and Recognition of Science and Technology) organization was founded to increase the public's 
awareness of engineering and technology in everyone's daily lives and as a career path for college bound students.<br />&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Our team is 
from Lowell, Michigan, a city about 10 miles east of Grand Rapids.  Last year was our second year of participation in the FRC, and we are hoping for a fantastic 
third year.  We had a very enthusiastic and dedicated group of students and mentors last year, and made it to the Michigan state championship.  Our team is made 
possible by our very generous sponsors, <a href="http://www.wbtooling.com" >White's Bridge Tooling</a>, <a href="http://www.grfoundation.org/lowell">Lowell Area Community Fund</a>.
We strive to realize "gracious professionalism", and we reach out to other teams and our community whenever we can.<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Explore our website!  Everything is student-designed and maintained.  Our website team is 
busy at work, always adding new pages and features.<br />

